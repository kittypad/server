	private Map<String, Map<String, Map<String, String>>> codMappings;

	public LafasoCOD() {
		codMappings = new TreeMap<String, Map<String, Map<String, String>>>();
		for (String[] location : COD_STR) {
			String province = getProvinceName(location[0]);
			String city = getCityOrDistrictName(location[1]);
			String district = getCityOrDistrictName(location[2]);
			if (!codMappings.containsKey(province))
				codMappings.put(province,
						new TreeMap<String, Map<String, String>>());
			Map<String, Map<String, String>> cities = codMappings.get(province);
			if (!cities.containsKey(city))
				cities.put(city, new TreeMap<String, String>());
			Map<String, String> districts = cities.get(city);
			districts.put(district, location[2]);
		}
	}

	@Override
	public int supportCOD(UserAddress address) {
		String province = getProvinceName(address.getProvince());
		String city = getCityOrDistrictName(address.getCity());
		String district = getCityOrDistrictName(address.getDistrict());
		if (!codMappings.containsKey(province))
			return NONSUPPORT;
		Map<String, Map<String, String>> cities = codMappings.get(province);
		// System.out.println(cities);
		if (!cities.containsKey(city))
			return NONSUPPORT;
		Map<String, String> districts = cities.get(city);
		// System.out.println(districts);
		if (districts.isEmpty())
			return SUPPORT;
		if (!districts.containsKey(district))
			return NONSUPPORT;
		if (containsBracket(districts.get(district)))
			return PARTIALLY_SUPPORT;
		return SUPPORT;
	}

	@Override
	public String supportArea(UserAddress address) {
		String province = getProvinceName(address.getProvince());
		String city = getCityOrDistrictName(address.getCity());
		String district = getCityOrDistrictName(address.getDistrict());
		Map<String, Map<String, String>> cities = codMappings.get(province);
		if (cities == null)
			return "";
		Map<String, String> districts = cities.get(city);
		if (districts == null)
			return "";
		String content = districts.get(district);
		if (content == null)
			return "";
		return content;
	}

